export const generateName = (sex: string) => {
  let firstName = fName[Math.floor(Math.random() * fName.length)];

  if (sex === "male") {
    firstName = mName[Math.floor(Math.random() * mName.length)];
  }

  const lastName = surName[Math.floor(Math.random() * surName.length)];

  return `${firstName} ${lastName}`;
};

export const generateRandomName = () => {
  const sex: Sex = Math.floor(Math.random() * 100) > 50 ? "male" : "female";
  return generateName(sex);
};
